public class HotCompanyBean {
    private String econName;
    private String econNo;
    private String hot;
    private String operName;
    private String sequential;
    private String uuid;
    public String getEconName() {
        return econName;
    }

    public void setEconName(String econName) {
        this.econName = econName;
    }

    public String getEconNo() {
        return econNo;
    }

    public void setEconNo(String econNo) {
        this.econNo = econNo;
    }

    public String getHot() {
        return hot;
    }

    public void setHot(String hot) {
        this.hot = hot;
    }

    public String getOperName() {
        return operName;
    }

    public void setOperName(String operName) {
        this.operName = operName;
    }

    public String getSequential() {
        return sequential;
    }

    public void setSequential(String sequential) {
        this.sequential = sequential;
    }

    public String getUuid() {
        return uuid;
    }

    public void setUuid(String uuid) {
        this.uuid = uuid;
    }


}
